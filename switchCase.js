// Casos possiveis quando uma condição for vendadeira
//swite case
var mes = "fereiro";

switch (mes) {
  case "fereiro":
    console.log("mes 2");
    break;

  case "Janeiro":
    console.log("mes 1");
    break;

  case "março":
    console.log("mes 3");
    break;

  default:
    console.log("nenhum dos casos acima foram atendidos");
}
